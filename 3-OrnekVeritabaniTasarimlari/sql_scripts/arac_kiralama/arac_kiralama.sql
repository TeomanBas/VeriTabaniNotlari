CREATE DATABASE `arac_kiralama` CHARACTER SET utf8 COLLATE utf8_unicode_ci;

CREATE TABLE MUSTERI_TABLOSU(
M_NO INT AUTO_INCREMENT PRIMARY KEY,
M_ADI VARCHAR(20),
M_SOYADI VARCHAR(20),
M_ADRES VARCHAR(20),
M_TELEFON VARCHAR(20)
)AUTO_INCREMENT=1;

CREATE TABLE ARAC_TABLOSU(
ARAC_NO INT  AUTO_INCREMENT PRIMARY KEY,
MODEL INT,
MARKA VARCHAR(20),
PLAKA VARCHAR(20),
FIYAT DOUBLE
)AUTO_INCREMENT=1;


CREATE TABLE SATIS_TABLOSU(
SAT_NO INT AUTO_INCREMENT PRIMARY KEY,
M_NO INT,
ARAC_NO INT,
SATIS_TARIHI DATE,
SATIS_FIYATI DOUBLE,
FOREIGN KEY (M_NO) REFERENCES MUSTERI_TABLOSU(M_NO),
FOREIGN KEY (ARAC_NO) REFERENCES ARAC_TABLOSU(ARAC_NO)
)AUTO_INCREMENT=1;

CREATE TABLE ALIM_TABLOSU(
ALIM_NO INT AUTO_INCREMENT PRIMARY KEY,
M_NO INT,
ARAC_NO INT,
ALIM_TARIHI DATE,
ALIS_FIYATI DOUBLE,
FOREIGN KEY (M_NO) REFERENCES MUSTERI_TABLOSU(M_NO),
FOREIGN KEY (ARAC_NO) REFERENCES ARAC_TABLOSU(ARAC_NO)
)AUTO_INCREMENT=1;

